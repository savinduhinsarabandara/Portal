const searchInput = document.getElementById('devicesearch');
searchInput.addEventListener('input', function() {
    var searchTerm = this.value.toLowerCase();
    var divs = document.querySelectorAll('.devicesearchdiv');

    divs.forEach(function(div) {
        var songName = div.querySelector('.song-name').textContent.toLowerCase();
        var artistName = div.querySelector('.artist-name').textContent.toLowerCase();

        if (songName.includes(searchTerm) || artistName.includes(searchTerm)) {
            div.style.display = 'block';
        } else {
            div.style.display = 'none';
        }
    });
});




